package com.powerboot.system.response;

public class RechangeResponse {

    private String rechangeCount;

    private String againRechangeCount;

    private String rechangeRate;

    private String againRechangeRate;

    public String getAgainRechangeRate() {
        return againRechangeRate;
    }

    public void setAgainRechangeRate(String againRechangeRate) {
        this.againRechangeRate = againRechangeRate;
    }

    public String getRechangeCount() {
        return rechangeCount;
    }

    public void setRechangeCount(String rechangeCount) {
        this.rechangeCount = rechangeCount;
    }

    public String getAgainRechangeCount() {
        return againRechangeCount;
    }

    public void setAgainRechangeCount(String againRechangeCount) {
        this.againRechangeCount = againRechangeCount;
    }

    public String getRechangeRate() {
        return rechangeRate;
    }

    public void setRechangeRate(String rechangeRate) {
        this.rechangeRate = rechangeRate;
    }
}
